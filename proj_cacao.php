                <h2>Cacao experiments</h2>
                <hr class="star-primary">    

                <p>
                  i liked to drink cacao and ate chocolates since childhood. and during whole my life i'm having it more or less. but since 2014 i realized that i'm kinda passionate about cacao and while i did travelling around South Asia i especially tried different types of cacao products to find out about quality and taste.
                </p>
                <p>
                  suddenly in 2015 i was at one indonesian island and surprisengly i discover a lots of cacao trees and pods there! that was amazing breakthrough. so i got some beans to <b>make first chocolate product by my own hands</b>
                  .
                </p>
                <p>
                  ohhh guys, that was the second really stunning unforgettable expirience in my life after <b>Salvia</b>
                  ! so than i decide to find out the way to deal with cacao beans directly and made my own experiments with it
                </p>
                <p>
                  in 2016 in <abbr title = "see the map below">sulya, karnataka, india</abbr>
                  i was lucky to get in touch with local farmer who is growing cacao trees. since that time already i got around <u>100kg (2 batches) of pure raw wet cacao beans</u>
                  and prepared it in Arambol, Goa
                </p>
                <p>
                  most of them was given as part of mine <abbr title="literally it's an Act of giving free food (mostly raw vegetables and fruits)&#013; on the Arambol's beach (Goa, India) during sunset time.&#013; was done around 80 times">"sunset-missions"</abbr>
                  and distributed for
                  <b>donations</b>
                  + sended to friends in Russia
                  <br/>    
                </p>
                <div class="bs-callout bs-callout-warning">
                  <h4>Cacao for DONATIONS</h4>
                        <a class="fancybox-thumbs" data-imagelightbox="c12" href="img/cacao_infographic.jpg">
                          <img src="img/cacao_infographic_800.jpg"  alt="cacao for donations" title="cacao for donations" class="img-responsive"></a>
                  <p>
                    it was
                    <b>pure experiment</b>
                    for me &mdash; just to check how ready is the people to make benefactions. i guess, totally around <u>25 times</u>
                    i distributed cacao-products at Arambol beach. and asked for donations only 2-3 times. most of the times people just took it and day-by-day started to give some money.
                    <br/>    
                    <b>so finally i got
                      <span class = "badge red">~5500 Rs</span>
                      of donations!</b> 
                    which is very very good start and it means that i nearly cover expenses for beans itself
                  </p>
                  <p>
                    also some people want to BUY cacao-beans and i keep telling them, that with this cacao i want:
                  </p>
                  <div class = "alert alert-success">to give and share rather than to sale and deal</div>
                  <p>
                    but of course you can get beans for money also if you don't know any better way to THANK. i understand, that for some people it's easier and habitual.
                  </p>
                  <p>
                    that's why i'm trying to maximize transparency so everybody could see what is the
                    <b>real price</b>
                    and how it comes from
                    <div class="p_10"></div>
                    Here is some calculation which make sense:
                    <br/>    
                    &nbsp;&nbsp;&nbsp;&nbsp;&mdash; 1kg of raw beans from farmer
                    <span class = "badge">~62 Rs/kg</span>
                    . one batch is
                    <u>~50kg</u>
                    &rarr;
                    <span class = "badge">~3100 Rs</span>
                    <br/>    
                    &nbsp;&nbsp;&nbsp;&nbsp;&mdash; transportation the batch from farmers place to goa (one way)
                    <span class = "badge">~500 Rs</span>
                    <br/>    
                    &nbsp;&nbsp;&nbsp;&nbsp;&mdash; drying beans costs
                    <span class = "badge">0 Rs</span>
                    (1kg of raw beans &rarr; ~0.3kg of dry beans). so from batch we can get upto
                    <u>18kg</u>
                    of dry beans
                    <br/>    
                    &nbsp;&nbsp;&nbsp;&nbsp;&mdash; roasting costs
                    <span class = "badge">~300 Rs</span>
                    per batch. it's included only charcoals and kerosene
                    <br/>    
                    &nbsp;&nbsp;&nbsp;&nbsp;&mdash; cleaning from nibs costs
                    <span class = "badge">0 Rs</span>
                    <br/>    
                    &nbsp;&nbsp;&nbsp;&nbsp;&mdash; grinding beans to cacao-mass costs
                    <span class = "badge">0 Rs</span>
                    <br/>    
                    &nbsp;&nbsp;&nbsp;&nbsp;&mdash; extra ingridients costs are very depends on stuff (fruits, olive oil, fullcream, honey, raisins etc). but i would say
                    <span class = "badge">~1200 Rs</span>
                    per batch
                    <br/>    
                    &nbsp;&nbsp;&nbsp;&nbsp;&mdash; using other facilities like cooking-gas or electricity is nearly 0 Rs till now. but for serious production it will be i guess
                    <span class = "badge">~200 Rs</span>
                    per batch/month
                  </p>
                  <p>
                    <b>Total cost</b> <font style="color: red;">*</font>
                    <br/>    
                    &nbsp;&nbsp;&nbsp;&nbsp;&mdash; for dryed beans
                    <span class = "badge green">~200 Rs/kg</span>
                    <br/>    
                    &nbsp;&nbsp;&nbsp;&nbsp;&mdash; for chocolate products
                    <span class = "badge green">~300 Rs/kg</span>
                    <br/>    
                  </p>
                  <p> <i><font style="color: red;">*</font>
                      it's just approx minimum cost with rough calculations</i> 
                  </p>

                </p>
