                  <a href = "#" class="list-group-item">
                    <h4 class = "list-group-item-heading">old-fashioned coffee roaster cylinder</h4>

                    <p class = "list-group-item-text">
                      <p>
                        this device i did order from one tinsmith at Mysore's hardware market. it was nearly impossible to get one, cuz only few people would understand what i'm asking about. so nowday only electrical/gas roasters in stock.
                      </p>
                      <p>
                        CONSTRUCTION &mdash; cylinder with handle and few holes on both sides. there is a hatch where you will load beans (capacity is ~1kg). and the rest is a barbeque grill with section for charcoals
                      </p>
                      <p>
                        OPERATING &mdash; just load beans in cylinder and heat charcoal in underneath section. slowly spining until smell and crackling will start coming out. also you can notice the readyness by color of beans itself
                      </p>
                      <p>DISADVANTAGES</p>
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; too much time (25-30 minutes)
                      <br/>    
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; too little capacity for mass production
                      <br/>    
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; no standart formula to prepare it exactly the same as before, cuz you couldn't rely on charcoals and you couldn't count "proper" rotation per minute
                      <br/>    
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; it takes all your attention and you couldn't do anything else during roasting
                      <br/>    
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; without any special coatings it starts rusting very very quickly
                      <br/>    
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; this particular construction has no "curtains" to prevent blowing-up heat from charcoals. it means if there is good wind, the charcoals will lasting very short time and most of the heat will blow away
                      <div class="p_10"></div>
                      <p>ADVANTAGES</p>
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; it's nice time spending process and it's kinda "magical" ceremony which could gather people
                      <br/>    
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; you still can adjust the readyness by opening hatch and checking bean-by-bean oftenly
                      <br/>    
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; you can use this barbeque stall with grill, charcoal section and cylinder for cooking anything else! like we made tea and roasting cashew nuts! it's very-very handy multi-purposal tool
                      <br/>    
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; it's not rely on electricity, so you can do it
                      <u>anywhere at anytime</u>
                      . and it's not so heavy and not big to carry around
                    </p>
                  </a>

                  <a href = "#" class="list-group-item">
                    <h4 class = "list-group-item-heading">old-fashioned coffee grinder</h4>

                    <p class = "list-group-item-text">
                      <p>
                        i was extremly lucky to found this device suddenly at Mysore's second-hand metal/garbage market. it was full of rust and was looks like rubbish, but i knew it could be very useful and i need to give it to try
                      </p>
                      <p>
                        CONSTRUCTION &mdash; this is like oldschool mincer/grinder with spiral knife and handle (looks nearly the same as russian meat mincer) but on the outcoming part there is special adjustable metal mills, which let you control the size of granules/particles
                      </p>
                      <p>
                        OPERATING &mdash; just load beans in  top section and start rotating the handle. you need to bring some hand-efford to get tough beans grinded. you will feel when it's over and you need to load another portion.
                      </p>
                      <p>
                        for first round i adjusting the gap between mills to get bigger granules from beans, and another round i'm making smaller gap to work with roughly grinded mass
                      </p>
                      <p>DISADVANTAGES</p>
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; unfortunately this mincer is not suits very good for fatty cacao beans, cuz it still heat them up while doing grinding and that it becames a greasy mass which is stucking inside the output mill. so you still can do it, but need to open it and clean very oftenly, which is not efficient at all
                      <br/>    
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; also you couldn't get unnoticeably smallest particles with this tool again because of fatness of cacao mass and specificity of mill/grinder
                      <br/>    
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; the capacity is very small (~200gramms per time) and also you should clean the tool serveral time + you should make at least 2 round to get smaller particles - so it takes a looooots of time to make some amount of cacao-mass
                      <div class="p_10"></div>
                      <p>ADVANTAGES</p>
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; it's good physical excercise for me _) and you can connect handle to b-cycle and spin it with legs as well!
                      <br/>    
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; it's not rely on electricity, so you can carry it around and grind cacao
                      <u>anywhere</u>
                      <br/>    
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; it's also like true interacting with cacao-beans to converting your love, passion and effort to something soulful and tastful. IMHO with electrical machine you could achieve technically right smallest particles, but you couldn't achieve that "connection" with what you are doing
                    </p>
                  </a>
