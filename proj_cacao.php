                <div id="sm1" class="sublinks collapse">
                  <a href = "#" class="list-group-item">
                    <h4 class = "list-group-item-heading">fresh raw wet beans (2 days old from pod)</h4>

                    <p class = "list-group-item-text">
                      <p>
                        TASTING &mdash;
                        <b>phew!</b>
                      </p>
                      <p>
                        DISTRIBUTING &mdash; some people told me that they liked it (~
                        <span class="pp crowd" data-val="15"></span>
                        15 % of crowd)
                      </p>
                    </p>
                  </a>

                  <a href = "#" class = "list-group-item">
                    <h4 class = "list-group-item-heading">sprouting raw beans (2 days old from pod)</h4>
                    <p class = "list-group-item-text">
                      <p>
                        one guy tried to sprout some undried beans &mdash; after 3 days he told me that there is
                        <u>no result</u>
                        . i guess maybe he got to less beans and not all of them was ready for sprouting or he did it wrong
                      </p>
                    </p>
                  </a>

                  <a href = "#" class = "list-group-item">
                    <h4 class = "list-group-item-heading">unfermenting & driying under the sun (upto 3 weeks)</h4>
                    <p class = "list-group-item-text">
                      <p>
                        longer is better for reduce bitterness, which is essential cuz those type of cacao (Forastero) is very avarage.
                      </p>
                      <p>
                        fermentation is also about reducing bitterness and make taste more mild, but i didn't make it yet
                      </p>
                      <p>
                        TASTING &mdash; for me it was unpleasant (cuz it's kinda "crappy bitter plasticine")
                      </p>
                      <p>
                        DISTRIBUTING &mdash; was very surprised that a lot of people (~
                        <span class="pp crowd" data-val="80"></span>
                        80 % of crowd) seems to
                        <u>like it!</u>
                        (comparing with roasted ones). and it's also perfect for
                        <b>vegans</b>
                        (cuz it's raw pure unprocessed stuff)
                      </p>
                    </p>
                  </a>

                  <a href = "#" class = "list-group-item">
                    <h4 class = "list-group-item-heading">
                      roasting with
                      <abbr title="see more details about it below">special cylindric manual roaster</abbr>
                    </h4>
                    <p class = "list-group-item-text">
                      <p>
                        around 25-30 minutes of slooowly manualy rotation of cylinder. very meditative process ) i did around
                        <u>8 times on the beach</u>
                        . it's also like "roasting ceremony" wich gathered few people few times. afterwards we use same charcoals to roasting cashew or preparing tea
                      </p>
                      <p>
                        TASTING &mdash; i like to eat roasted beans a lot! especially i did a very darkly roasting (on the edge of burning) so the taste becames very rough & tough. it have very very
                        <u>intense</u>
                        taste (cuz it's around 95% of pure cacao, guys!) and you couldn't have it more than one/two tea-spoons at the time. also it's a good energetic and hunger-killer
                      </p>
                      <p>
                        DISTRIBUTING &mdash; not so many people (~
                        <span class="pp crowd" data-val="30"></span>
                        30 % of crowd) really like it. most of them said it's over-roasted and more like charcoal than cacao
                      </p>
                      <p>COOKING</p>
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; with bananas /chickoo /dates /raisins /honey as natural sweeteners
                      <br/>    
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; with milk /olive oil /coconut oil /groundnut oil /sunflower oil /sesame seed oil /fullcream /whisky as diluent
                      <br/>    
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; with brown sugar /sugarcane solid (like panela) /condensed milk for artificial sweeteners
                      <br/>    
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; with chilli powder /vanilla /cinammon as additional dimensions for taste
                      <br/>    
                      <p>
                        <div class = "alert alert-success">there is nearly infinity ways and combinations</div>
                      </p>
                      <p>STRUCTURE</p>
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; usually it becames as crumbly/crunchy wet mass, which is melting in normal warm temperature and drying out very quickly (became more crumbly)
                      <br/>    
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; when i mixed with whisky it becames as solid balls
                      <br/>    
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; once i made kinda mousse (added milk, sugar and steaming bath)
                      <br/>    
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; few times made it solid by steaming bath and cooling at fridge. but again it will melt and dry in normal temperature and the taste will evaporate quite quickly
                      <br/>    
                      &nbsp;&nbsp;&nbsp;&nbsp;&mdash; few times did cacao-drink. it's very-very creamy/greasy, dense and intense. but i like it!
                    </p>
                  </a>
